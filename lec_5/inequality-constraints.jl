md"$f(x) = \frac{1}{2} (x - c)^T Q (x - c)$"

# ╔═╡ 8e927015-f612-4589-a5ed-5635e277f125
begin
	Q = Diagonal([0.5; 1])
	function f(x)
	    return 0.5*(x-[1; 0])'*Q*(x-[1; 0])
	end
	function ∇f(x)
	    return Q*(x-[1; 0])
	end
	function ∇2f(x)
	    return Q
	end
end

# ╔═╡ 1261b5a3-f73f-4667-83c1-f236b4962c80
begin
	A = [1.0 -1.0]
	b = -1.0
	function c(x)
	    return dot(A,x) - b
	end
	function ∂c(x)
	    return A
	end
end

# ╔═╡ 8f6fe1e6-1121-41fb-ab50-b49a3c738a7f
function plot_landscape()
    Nsamp = 20
    Xsamp = kron(ones(Nsamp),LinRange(-4,4,Nsamp)')
    Ysamp = kron(ones(Nsamp)',LinRange(-4,4,Nsamp))
    Zsamp = zeros(Nsamp,Nsamp)
    for j = 1:Nsamp
        for k = 1:Nsamp
            Zsamp[j,k] = f([Xsamp[j,k]; Ysamp[j,k]])
        end
    end
    contour(Xsamp,Ysamp,Zsamp)

    xc = LinRange(-4,3,Nsamp)
    plot(xc,xc.+1,"y")
end

# ╔═╡ 20aded0e-4551-45d1-a100-1e65cbb8c56c
plot_landscape()

